print("--- Hitung Rata-rata ---")
# Meminta 4 nilai
n1 = float(input("Masukkan nilai ke-1: "))
n2 = float(input("Masukkan nilai ke-2: "))
n3 = float(input("Masukkan nilai ke-3: "))
n4 = float(input("Masukkan nilai ke-4: "))

# Menghitung rata-rata
rata_rata = (n1 + n2 + n3 + n4) / 4

print(f"Rata-rata dari keempat nilai tersebut adalah: {rata_rata}")